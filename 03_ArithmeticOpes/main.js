/*
  Toán tử số học (Arithmetic Operators)
  - Cộng (+), trừ (-), nhân (*), chia (/), chia lấy dư (%), lũy thừa (**)
  - Tăng (++) và giảm (--) giá trị của biến
*/

// Ví dụ về các toán tử số học
let a = 5;
let b = 2;

console.log(a + b);     // 7 - cộng
console.log(a - b);     // 3 - trừ
console.log(a * b);     // 10 - nhân
console.log(a / b);     // 2.5 - chia
console.log(a % b);     // 1 - chia lấy dư
console.log(a ** b);    // 25 - lũy thừa (5^2)


/*
  Ghi chú:
  - % thường dùng để kiểm tra số chẵn/lẻ: số % 2 === 0
  - ** dùng thay cho Math.pow(a, b)
*/
